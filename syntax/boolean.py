# Number
import os.path
print(1)   # Integer
# String
print('Hello world')
# Boolean
print(True)   # 참, 1
print(False)   # 거짓, 0
# Expression
print(1+1)   # 2
print('Hello '+'world')   # Hello world
# Comparison operator : 비교 연산자
print(1 == 1)   # True
print(1 < 2)   # True
print(2 < 1)   # False
# Membership operator
print('world' in 'Hello world')   # True
